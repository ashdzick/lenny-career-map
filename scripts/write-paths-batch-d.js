function set(from, to, lines) {
  const key = `${from}|||${to}`;
  if (!paths[key]) {
    console.warn(`Key not found: ${key}`);
    return;
  }
  paths[key].content = lines.join("\n");
  paths[key].generatedAt = "2026-03-29";
}

